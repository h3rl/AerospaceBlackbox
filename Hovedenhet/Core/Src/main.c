/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  init();
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_FDCAN1_Init();
  MX_SPI1_Init();
  MX_USART3_UART_Init();
  MX_UART5_Init();
  MX_UART8_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  HAL_UART_Receive_IT(CAM1.huart, CAM1.Status, 2);
  HAL_UART_Receive_IT(CAM2.huart, CAM2.Status, 2);
  HAL_UART_Receive_IT(CAM3.huart, CAM3.Status, 2);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */


  Flash_Init(0);
  Flash.ID=Read_ID();
  while (1)
  {
	  Read_Register(SR);
	  HAL_UART_Receive(&huart3, &command,1, 100);

	  //CAM to IDLE
	  if(command==0x41){
		  command_cam(CAM1, IDLE);
		  command_cam(CAM2, IDLE);
		  command_cam(CAM3, IDLE);
		  command=0;
	  }

	  //CAM to REC
	  if(command==0x42){
		  command_cam(CAM1, REC);
		  command_cam(CAM2, REC);
		  command_cam(CAM3, REC);
		  command=0;
	  }

	  //CAM to FORMAT
	  if(command==0x43){
		  command_cam(CAM1, FORMAT);
		  command_cam(CAM2, FORMAT);
		  command_cam(CAM3, FORMAT);
		  command=0;
	  }

	  //CAM to REBOOT
	  if(command==0x44){
		  command_cam(CAM1, REBOOT);
		  command_cam(CAM2, REBOOT);
		  command_cam(CAM3, REBOOT);
		  command=0;
	  }

	  //CAM to DEB
	  if(command==0x45){
		  command_cam(CAM1, DEB);
		  command_cam(CAM2, DEB);
		  command_cam(CAM3, DEB);
		  command=0;
	  }

	  //Reboot MCU
	  if(command==0x47){
		  NVIC_SystemReset();
		  command=0;
	  }

	  //Start GoPro filming
	  if(command==0x48){
		  HAL_GPIO_WritePin (GPIOD, GOPRO_Pin, GPIO_PIN_SET);
		  GoPro=1;
		  command=0;
	  }

	  //Stop GoPro filming
	  if(command==0x49){
		  HAL_GPIO_WritePin (GPIOD, GOPRO_Pin, GPIO_PIN_SET);
		  GoPro=1;
		  command=0;
	  }

	  //Turn on GoPro
	  if(command==0x4A){
		  HAL_GPIO_WritePin (GPIOD, GOPRO_Pin, GPIO_PIN_SET);
		  GoPro=1;
		  command=0;
	  }

	  //Turn off GoPro
	  if(command==0x4B){
		  HAL_GPIO_WritePin (GPIOD, GOPRO_Pin, GPIO_PIN_SET);
		  GoPro=1;
		  command=0;
	  }

	  //Erase flight REC
	  if(command==0x4C){
		  Chip_Erase();
		  command=0;
	  }

	  //Start fligt REC
	  if(command==0x4D){
		  CAN_SendMessage(0x100);
		  Start_Flight_Recording=1;
		  command=0;
	  }

	  //Stop flight REC
	  if(command==0x4E){
		  Start_Flight_Recording=0;
		  command=0;
	  }

	  //Read flight REC
	  if(command==0x52){
		  Read_Data_Cont(16);
		  command = 0;
	  }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

